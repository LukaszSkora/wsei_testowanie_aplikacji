package wsei.testowanieAplikacji;

public class KonwersjaJednostekRozszerzona {

    public double kgToPound(double kg) {
        return kg * 2.20462;
    }

    public double mileToKm(double mile) {
        return mile * 1.609344;
    }

    public double literToGallon(double liter) {
        return liter * 0.2641720524;
    }

    public double literToOunce(double liter) {
        return liter * 35.19508;
    }

    public double literToPint(double liter) {
        return liter * 1.759754;
    }
}
